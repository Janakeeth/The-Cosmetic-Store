    private void UpdatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdatebtnActionPerformed
        int selectedRow = StockTable.getSelectedRow();
        if (selectedRow != -1) {
            String newStockStr = Stocktf.getText();
            if (!newStockStr.isEmpty()) {
                try {
                    int newStock = Integer.parseInt(newStockStr);
                    DefaultTableModel model = (DefaultTableModel) StockTable.getModel();
                    model.setValueAt(newStock, selectedRow, 2);
                    try {
            FileInputStream file = new FileInputStream("product.bin");
            ObjectInputStream input = new ObjectInputStream(file);

            ArrayList<Product> products = (ArrayList<Product>) input.readObject();

            input.close();
            file.close();

            // Update the stock of the product in the ArrayList
            Product product = products.get(selectedRow);
            product.setStock(newStock);

            // Write the updated data back to the file
            FileOutputStream fileOut = new FileOutputStream("product.bin");
            ObjectOutputStream objectOut = new ObjectOutputStream(fileOut);
            objectOut.writeObject(products);
            objectOut.close();
            fileOut.close();

        } catch (IOException | ClassNotFoundException e) {
            e.printStackTrace();
        }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Invalid stock count! Please enter a valid integer.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please enter a new stock count.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a product to update.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_UpdatebtnActionPerformed

