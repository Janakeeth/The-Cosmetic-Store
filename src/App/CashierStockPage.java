    private void SearchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchbtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) StockTable.getModel();
        TableRowSorter<DefaultTableModel> obj = new TableRowSorter<>(model);
        StockTable.setRowSorter(obj);
        obj.setRowFilter(RowFilter.regexFilter(SearchField.getText()));
    }//GEN-LAST:event_SearchbtnActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
        FileInputStream file = new FileInputStream("product.bin");
        ObjectInputStream input = new ObjectInputStream(file);
        
        ArrayList<Product>products = (ArrayList<Product>) input.readObject();

        input.close();
        file.close();

        DefaultTableModel model = (DefaultTableModel) StockTable.getModel();
        for (Product product : products) {
            model.addRow(new Object[]{product.getID(), product.getName(), product.getStock()});
        }
        } catch (IOException | ClassNotFoundException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

